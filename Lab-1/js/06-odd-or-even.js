// Loop through numbers from 0 to 15
for (let i = 0; i <= 15; i++) {
    // Check if the number is even
    if (i % 2 === 0) {
        console.log(i + " is even"); // Print the number and indicate it is even
    } else {
        console.log(i + " is odd"); // Print the number and indicate it is odd
    }
}
